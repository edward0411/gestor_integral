                    </div>
                    <p>
                        En cumplimiento de lo establecido en la Ley, tustareas.com.co lleva a cabo el Tratamiento de
                        Datos Personales respetando los siguientes principios generales:
                    </p>
                    <p>&nbsp; &nbsp; &nbsp; &nbsp;<i class="fa fa-check" aria-hidden="true"></i><b>&nbsp; &nbsp;&nbsp;
                            &nbsp;
                            &nbsp;Principio de legalidad en materia de tratamiento de datos:</b>El Tratamiento de Datos
                        Personales es una actividad reglada que debe sujetarse a lo establecido en la Ley 1581 de 2012 y
                        en las demás disposiciones que la desarrollan o reglamentan.</p>

                    <p>&nbsp; &nbsp; &nbsp; &nbsp;<i class="fa fa-check" aria-hidden="true"></i><b>&nbsp; &nbsp;&nbsp;
                            &nbsp;
                            &nbsp;Principio de finalidad:</b>El Tratamiento de los Datos Personales recogidos debe
                        obedecer a una finalidad legítima de acuerdo con la Constitución y la Ley, la cuál debe ser
                        informada al Titular.</p>
                    <p>&nbsp; &nbsp; &nbsp; &nbsp;<i class="fa fa-check" aria-hidden="true"></i><b>&nbsp; &nbsp;&nbsp;
                            &nbsp;
                            &nbsp;Principio de libertad:</b>El Tratamiento solo podrá ejercerse con el consentimiento,
                        previo, expreso e informado del Titular. Los Datos Personales no podrán ser obtenidos o
                        divulgados sin previa autorización o en ausencia de mandato legal o judicial que releve el
                        consentimiento. Principio de veracidad o calidad: La información sujeta a Tratamiento debe ser
                        veraz, completa, exacta, comprobable y comprensible. No será efectuado el Tratamiento de datos
                        parciales, incompletos, fraccionados o que induzcan a error.</p>
                    <p>&nbsp; &nbsp; &nbsp; &nbsp;<i class="fa fa-check" aria-hidden="true"></i><b>&nbsp; &nbsp;&nbsp;
                            &nbsp;
                            &nbsp;Principio de transparencia:</b>En el Tratamiento debe garantizarse el derecho del
                        Titular a obtener de las Empresas, en cualquier momento y sin restricciones, información acerca
                        de la existencia de datos que le conciernan. Principio de acceso y circulación restringida: El
                        Tratamiento de datos se sujeta a los límites que se derivan de la naturaleza de los Datos
                        Personales, así como de las disposiciones de la Ley. En este sentido, los Datos Personales,
                        salvo la información pública, y lo dispuesto en la autorización otorgada por el Titular del
                        dato, no podrán estar disponibles en Internet u otros medios de divulgación o comunicación
                        masiva, salvo que el acceso sea técnicamente controlable para brindar un conocimiento
                        restringido sólo a los Titulares o terceros autorizados.</p>
                    <p>&nbsp; &nbsp; &nbsp; &nbsp;<i class="fa fa-check" aria-hidden="true"></i><b>&nbsp; &nbsp;&nbsp;
                            &nbsp;
                            &nbsp;Principio de seguridad:</b>La información sujeta a Tratamiento por parte de
                        tustareas.com.co se deberá manejar con las medidas técnicas, humanas y administrativas que sean
                        necesarias para otorgar seguridad a los registros evitando su adulteración, pérdida, consulta,
                        uso o acceso no autorizado o fraudulento</p>
                    <p>&nbsp; &nbsp; &nbsp; &nbsp;<i class="fa fa-check" aria-hidden="true"></i><b>&nbsp; &nbsp;&nbsp;
                            &nbsp;
                            &nbsp;Principio de confidencialidad:</b>Todas las personas que intervengan en el Tratamiento
                        de Datos Personales que no tengan la naturaleza de públicos están obligadas a garantizar la
                        reserva y confidencialidad de la información, inclusive después de finalizada su relación con
